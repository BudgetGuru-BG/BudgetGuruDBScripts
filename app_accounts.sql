--function for creating the app_account_audit_record
CREATE OR REPLACE FUNCTION PUBLIC.CREATE_APP_ACCOUNT_AUDIT_RECORD()
  RETURNS TRIGGER 
  LANGUAGE PLPGSQL
AS $FUNCTION$
DECLARE
	_APP_ACCOUNT_ID BIGINT;
	_TRANSCATION_ID BIGINT;
	_ACTION CHAR(1);
	_ROW_DATA JSONB;
	_ACTION_USER_ID TEXT;
	_ACTION_USER_NAME TEXT;
	_ACTION_TIME_STAMP TIMESTAMP;
	_STATEMENT_TIME_STAMP TIMESTAMP;
BEGIN
	IF TG_WHEN <> 'AFTER' THEN 
		RAISE EXCEPTION 'THE CREATE ACCOUNT AUDIT RECORD FUNCTION CAN ONLY BE USED AS AN AFTER TRIGGER';
	END IF;

	_APP_ACCOUNT_ID = COALESCE(OLD.ID, NEW.ID);
	_TRANSCATION_ID = txid_current();
	_ACTION = SUBSTRING (TG_OP, 1 , 1);
	_ACTION_USER_ID = GET_ACTION_USER_ID();
	_ACTION_USER_NAME = GET_ACTION_USER_NAME();
	_ACTION_TIME_STAMP = CURRENT_TIMESTAMP;
	_STATEMENT_TIME_STAMP = STATEMENT_TIMESTAMP();

	IF _ACTION = 'I' THEN
   			_ROW_DATA = ROW_TO_JSON(NEW);
	ELSIF _ACTION = 'U' THEN
			_ROW_DATA = GET_JSON_DIFF(ROW_TO_JSON (NEW):: JSONB, ROW_TO_JSON (OLD) ::JSONB);
	ELSIF _ACTION = 'D' THEN
			_ROW_DATA = ROW_TO_JSON (OLD);
	ELSE
			RAISE EXCEPTION 'INVALID ACTION % FOR CREATE ACCOUNT_AUDIT_RECORD FUNCTION.', TG_OP;
	END IF;

	INSERT INTO PUBLIC.APP_ACCOUNT_AUDIT_LOG(
		APP_ACCOUNT_ID, TRANSCATION_ID, ACTION, ROW_DATA, ACTION_USER_ID, ACTION_USER_NAME, ACTION_TIMESTAMP,STATEMENT_TIMESTAMP
	)
	VALUES (
		_APP_ACCOUNT_ID, _TRANSCATION_ID, _ACTION, _ROW_DATA, _ACTION_USER_ID, _ACTION_USER_NAME, _ACTION_TIME_STAMP, _STATEMENT_TIME_STAMP
	);
	RETURN NULL;

END;
$FUNCTION$
;


--trigger for app_account_audit
CREATE TRIGGER APP_ACCOUNT_AUDIT_TRIGGER AFTER
INSERT 
	OR
UPDATE
	OR
DELETE
	ON
	PUBLIC.APP_ACCOUNT FOR EACH ROW EXECUTE FUNCTION CREATE_APP_ACCOUNT_AUDIT_RECORD();
